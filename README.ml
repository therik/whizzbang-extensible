1: To install

git clone this repo

$ composer install

2: To print WhizzBang.

$ php -f index.php


3: To see other ways of printing whizzbang or something different

Go to index.php, rule declarations start at line 26.

There are three ways to generate WhizzBang under current set of rules. Uncomment one of them and comment out the others.

Same can be seen in tests/Functional/WhizzBangTest.php.

4: To run tests

$ composer test
